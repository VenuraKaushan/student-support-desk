package userTASK;

public class admin {

	private String aid;
	private String pass;
	private String name;
	
	public admin(String aid, String pass, String name) {
		super();
		this.aid = aid;
		this.pass = pass;
		this.name = name;
	}

	public String getAid() {
		return aid;
	}

	public String getPass() {
		return pass;
	}

	public String getName() {
		return name;
	}
	
	
}
